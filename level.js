import Brick from "./brick.js";

export function brickBuild(game,level) {
    let bricks =[];

    level.forEach((arr,i) =>{
        arr.forEach((brick,j) => {

            let brickPos = {
                x: 50 * j,
                y: 60 + 30 * i
            }
            if(brick == 1)
            bricks.push(new Brick(game,brickPos));
        });
    });

    return bricks;
}

